# Each PromptSpec is a compact tuple of:
#   (label_en, label_zh, full_en, full_zh, label_ja, label_ko, label_es)
# Long-form ja/ko/es prompts aren't included because the upstream
# providers (PiAPI Flux / Kling / Luma, Vertex Imagen) accept en or zh
# best — usePromptLibrary picks one of those two at submit time.
