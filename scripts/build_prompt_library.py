#!/usr/bin/env python3
"""
Deterministic, hand-curated prompt library for VidGo.

Unlike `generate_prompt_library.py` (Gemini-driven, output drifts every run),
this script generates a stable JSON file from Python data structures.
Each tool maps to a group of 40 prompts. The prompts are explicit,
provider-tested, and contain anti-hallucination guards.

Run from repo root:
    python3 scripts/build_prompt_library.py

It writes to:
    frontend-vue/src/data/prompt_library.json

Existing IDs are preserved across re-runs because we sort prompts
deterministically and use auto-assigned 3-digit ids per group.

Schema (v3 — same shape as v2):
{
  "_meta": {...},
  "tools": {
    "<tool_key>": {
      "label": {"en":..., "zh":..., "ja":..., "ko":..., "es":...},
      "prompts": [
        {
          "id": "<prefix>_NNN",
          "label": {"en":..., "zh":..., "ja":..., "ko":..., "es":...},
          "en":  "<full prompt>",
          "zh":  "<full prompt>"
        }, ...
      ]
    }
  }
}
"""
