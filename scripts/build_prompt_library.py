def main() -> None:
    out: dict = {
        "_meta": {
            "model": "vidgo_handcrafted_v3",
            "generated_at": int(time.time()),
            "version": hashlib.sha256(str(time.time()).encode()).hexdigest()[:8],
            "schema": "v3 — 10 tool groups, 40 prompts each, deterministic Python source of truth at scripts/build_prompt_library.py",
            "note": "Hand-curated for VidGo. Re-run scripts/build_prompt_library.py to regenerate.",
        },
        "tools": {},
    }

    # Category bands within pattern_generate. Driven by the section comments
    # in PATTERN_GENERATE (Geometric / Floral / Animal / Culture / Abstract,
    # 8 entries each, in that order). When the frontend filters the dropdown
    # by selected style, it joins on this `category` field.
    PATTERN_CATEGORY_BANDS: List[tuple[int, int, str]] = [
        (1, 8, "geometric"),
        (9, 16, "floral"),
        (17, 24, "animal"),
        (25, 32, "traditional"),
        (33, 40, "abstract"),
    ]

    def _pattern_category(idx: int) -> str:
        for lo, hi, cat in PATTERN_CATEGORY_BANDS:
            if lo <= idx <= hi:
                return cat
        return ""

    total = 0
    for tool_key, spec in TOOLS.items():
        prefix = spec["prefix"]
        label_en, label_zh, label_ja, label_ko, label_es = spec["label"]
        guards_en, guards_zh = spec["guards"]
        prompts: List[PromptSpec] = spec["prompts"]
        prompt_dicts: List[dict] = []
        for i, p in enumerate(prompts, start=1):
            full_en = (p.full_en + guards_en).strip()
            full_zh = (p.full_zh + guards_zh).strip()
            entry: dict = {
                "id": f"{prefix}_{i:03d}",
                "label": {
                    "en": p.label_en,
                    "zh": p.label_zh,
                    "ja": p.label_ja or p.label_en,
                    "ko": p.label_ko or p.label_en,
                    "es": p.label_es or p.label_en,
                },
                "en": full_en,
                "zh": full_zh,
            }
            if tool_key == "pattern_generate":
                entry["category"] = _pattern_category(i)
            prompt_dicts.append(entry)
        out["tools"][tool_key] = {
            "label": {"en": label_en, "zh": label_zh, "ja": label_ja, "ko": label_ko, "es": label_es},
            "prompts": prompt_dicts,
        }
        total += len(prompts)
        print(f"  {tool_key:32s} {len(prompts):3d} prompts ({prefix}_001 .. {prefix}_{len(prompts):03d})")

    OUT_PATH.write_text(json.dumps(out, ensure_ascii=False, indent=2) + "\n")
    print(f"\nWrote {total} prompts across {len(TOOLS)} groups → {OUT_PATH}")


if __name__ == "__main__":
    main()
